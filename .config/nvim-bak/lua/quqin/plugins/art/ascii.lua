-- Retrieve the current index ascii art
function ascii.get(i)
  if i == nil or i < 1 or i > #array then
    return array[1] -- Apply color to the first ASCII frame
  else
    return array[i] -- Apply color to the requested frame
  end
end

-- Retrieve the next ascii art
function ascii.next()
  index = (index % #array) + 1
  return ascii.get(index)
end

-- Moves ascii art to the center
function ascii.center(current)
  local width = vim.fn.winwidth(0)
  local lines = vim.split(current, "\n")
  local centered = {}

  for _, line in ipairs(lines) do
    local padding = math.max(0, math.floor((width - #line) / 2))
    table.insert(centered, string.rep(" ", padding) .. line)
  end
  return table.concat(centered, "\n")
end

return ascii
